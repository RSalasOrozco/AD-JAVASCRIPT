// los operadores de comparacion son los que nos permiten comparar valores, si son iguales o no.

let a = 10;
console.log(a > 5); // mayor que
console.log(a < 5); // menor que
console.log(a >= 5); // mayor o igual que
console.log(a <= 5); // menor o igual que
console.log(a == 5); // igual que, se encarga de comparar el valor
console.log(a === 5); // estrictamente igual que, se encarga de comparar el valor y el tipo
console.log(a != 5); // diferente que , se encarga de comparar el valor
